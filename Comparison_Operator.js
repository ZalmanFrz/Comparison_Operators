// Comparison Operators Example//

//== Double equal to 
console.log(3 == 3);

//!= not equal to
console.log(3 != 3);

console.log(3 > 4);
//> Greater Than 

// < Less Than 
console.log(3 < 4);

// > = Greater Than or equal to
console.log(3 >= 2);

// < = Less Than or equal to
console.log(3 <= 3);

// === Strictly equal to
console.log(3 === 3);

// !== Strictly not equal to
console.log(2 !== 3);